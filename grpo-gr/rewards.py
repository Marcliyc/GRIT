def zipngram(text: str, ngram_size: int):
    words = text.lower().split()
    return zip(*[words[i:] for i in range(ngram_size)])

def repetitive_reward(prompts, completions, completion_ids=None, gt_answer=None, **kwargs):
    """Reward function that checks if the completion has repeting workds."""
    ngram_size = 8
    max_reward = 0.5
    rewards = []
    pad_token_id = kwargs.get("pad_token_id")
    eos_token_id = kwargs.get("eos_token_id")
    answer_end_token_ids = kwargs.get("answer_end_token_ids") or []
    if completion_ids is None:
        return [max_reward for _ in range(len(completions))]
    # iterate over each generated sequence in the batch
    for ids, completion in zip(completion_ids or [], completions):
        
        # for words repeating
        
        if completion == "":
            rewards.append(max_reward)
            continue
        if len(completion.split()) < ngram_size:
            rewards.append(max_reward)
            continue
        repeat_count = 0
        total = 0
        tokens = completion.split()


        for i in range(len(tokens) - ngram_size):
            ng1 = tuple(tokens[i:i+ngram_size])
            ng2 = tuple(tokens[i+ngram_size:i+ngram_size+ngram_size])
            total += 1
            if ng1 == ng2:
                repeat_count += 1

        if total == 0:
            reward = 1.0
        else:
            reward = 1.0 - (repeat_count / total)
        
        
        
        
        # convert to Python list
        ids_list = ids.tolist()
        # trim by answer-end sequence first (if provided)
        if answer_end_token_ids and len(ids_list) >= len(answer_end_token_ids):
            end_idx = None
            seq_len = len(answer_end_token_ids)
            for j in range(len(ids_list) - seq_len + 1):
                if ids_list[j:j+seq_len] == answer_end_token_ids:
                    end_idx = j + seq_len
                    break
            if end_idx is not None:
                ids_list = ids_list[:end_idx]
        # then trim by eos or pad tokens
        for stop_id in [eos_token_id, pad_token_id]:
            if stop_id is not None and stop_id in ids_list:
                ids_list = ids_list[: ids_list.index(stop_id)]
        # if too short to form two full n‑grams, give full reward
        if len(ids_list) < 2 * ngram_size:
            rewards.append(max_reward)
            continue

        repeat_count = 0
        total_pairs = 0
        # compare each n‑gram with the immediately following one
        for i in range(len(ids_list) - 2*ngram_size + 1):
            ng1 = tuple(ids_list[i : i + ngram_size])
            ng2 = tuple(ids_list[i + ngram_size : i + 2*ngram_size])
            total_pairs += 1
            if ng1 == ng2:
                repeat_count += 1

        # if there were no valid comparisons, treat as non‑repetitive
        if total_pairs == 0:
            score = 1.0
        else:
            score = 1.0 - (repeat_count / total_pairs)

        # scale into [0, max_reward]
        rewards.append((score - (1-reward)) * max_reward)
    return rewards


def _extract_bboxes_from_text(text):
    segments = []
    if "<box>" in text and "</box>" in text:
        segments.append(text.split("<box>", 1)[1].split("</box>", 1)[0])
    if "bbox_2d" in text:
        segments.append(text)
    if not segments:
        segments = [text]

    pattern = re.compile(r"(-?\d+(?:\.\d+)?)\s*,\s*(-?\d+(?:\.\d+)?)\s*,\s*(-?\d+(?:\.\d+)?)\s*,\s*(-?\d+(?:\.\d+)?)")
    bboxes = []
    for segment in segments:
        for match in pattern.finditer(segment):
            bbox = [float(value) for value in match.groups()]
            bboxes.append(bbox)
    return bboxes


def _sanitize_bbox(bbox):
    x1, y1, x2, y2 = bbox
    x_min, x_max = sorted([x1, x2])
    y_min, y_max = sorted([y1, y2])
    if x_max <= x_min or y_max <= y_min:
        return None
    return (x_min, y_min, x_max, y_max)


def _union_area(rects):
    if not rects:
        return 0.0
    xs = sorted({rect[0] for rect in rects}.union({rect[2] for rect in rects}))
    area = 0.0
    for i in range(len(xs) - 1):
        x_left = xs[i]
        x_right = xs[i + 1]
        if x_right <= x_left:
            continue
        intervals = []
        for x1, y1, x2, y2 in rects:
            if x1 < x_right and x2 > x_left:
                intervals.append((y1, y2))
        if not intervals:
            continue
        intervals.sort()
        merged = []
        cur_start, cur_end = intervals[0]
        for start, end in intervals[1:]:
            if start <= cur_end:
                cur_end = max(cur_end, end)
            else:
                merged.append((cur_start, cur_end))
                cur_start, cur_end = start, end
        merged.append((cur_start, cur_end))
        y_total = sum(end - start for start, end in merged)
        area += (x_right - x_left) * y_total
    return area


def _intersection_union_areas(pred_rects, gt_rects):
    pred_area = _union_area(pred_rects)
    gt_area = _union_area(gt_rects)
    intersections = []
    for px1, py1, px2, py2 in pred_rects:
        for gx1, gy1, gx2, gy2 in gt_rects:
            ix1 = max(px1, gx1)
            iy1 = max(py1, gy1)
            ix2 = min(px2, gx2)
            iy2 = min(py2, gy2)
            if ix2 > ix1 and iy2 > iy1:
                intersections.append((ix1, iy1, ix2, iy2))
    intersection_area = _union_area(intersections)
    union_area = pred_area + gt_area - intersection_area
    return intersection_area, union_area


def grounded_region_bbox_giou_reward(prompts, completions, completion_ids=None, bboxs=None, width=None, height=None, normalized_bboxs=False, **kwargs):
    """Reward based on generalized IoU (GIoU) between predicted and ground-truth bbox unions."""
    rewards = []
    if bboxs is None:
        return [0.0 for _ in range(len(completions))]
    width_list = width or [None] * len(completions)
    height_list = height or [None] * len(completions)
    for response, gt_boxes, img_w, img_h in zip(completions, bboxs, width_list, height_list):
        if not gt_boxes:
            rewards.append(0.0)
            continue

        pred_boxes = _extract_bboxes_from_text(response)
        if normalized_bboxs and img_w and img_h:
            pred_boxes = [
                [box[0] * img_w, box[1] * img_h, box[2] * img_w, box[3] * img_h]
                for box in pred_boxes
            ]

        pred_rects = []
        for box in pred_boxes:
            rect = _sanitize_bbox(box)
            if rect:
                pred_rects.append(rect)

        gt_rects = []
        for box in gt_boxes:
            rect = _sanitize_bbox(box)
            if rect:
                gt_rects.append(rect)

        if not pred_rects or not gt_rects:
            rewards.append(0.0)
            continue

        intersection_area, union_area = _intersection_union_areas(pred_rects, gt_rects)
        if union_area <= 0:
            rewards.append(0.0)
            continue

        iou = intersection_area / union_area
        all_rects = pred_rects + gt_rects
        x_min = min(rect[0] for rect in all_rects)
        y_min = min(rect[1] for rect in all_rects)
        x_max = max(rect[2] for rect in all_rects)
        y_max = max(rect[3] for rect in all_rects)
        c_area = max(0.0, (x_max - x_min) * (y_max - y_min))
        if c_area <= 0:
            rewards.append(0.0)
            continue

        giou = iou - (c_area - union_area) / c_area
        rewards.append(giou)

    return rewards
