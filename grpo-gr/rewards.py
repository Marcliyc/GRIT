    keys = ["<answer>"] #  'Result:', '<submit>' '<request><SimpleCalculatorTool>', '<call>', '<response>', 
    for response, answer in zip(completions, gt_answer or []):
        reward = 0.0
        for key in keys:
            if key in response:
                reward += 1.0
                if len(response.split(key)) == 2:
                    reward += 1.0
        rewards.append(reward/len(keys)/2*max_reward)

    return rewards


